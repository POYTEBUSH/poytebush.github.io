function GetIcon(text){
    if(text === "c++"){
        return '<img src="https://img.icons8.com/color/50/000000/c-plus-plus-logo.png" class="img-fluid">'
    }
    else if(text === "c#"){
        return '<img src="https://img.icons8.com/color/50/000000/c-sharp-logo.png" class="img-fluid">'
    }
    else if(text === "github"){
        return '<img src="https://img.icons8.com/color/50/000000/github.png" class="img-fluid">'
    }
    else if(text === "visualstudio"){
        return '<img src="https://img.icons8.com/color/50/000000/visual-studio.png" class="img-fluid">'
    }
}